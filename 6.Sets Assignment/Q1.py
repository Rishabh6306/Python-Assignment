# Q1. What is a set in Python?


# Set is store collection of data in a single variable. We have also list and tuples these can do similar things. But we use set because sets cannot have multiple occurrences of the same element, it makes sets highly useful to efficiently remove duplicate values. 
# Ex:
s = {2, "Ram", 20 ,100, 50 ,20, "Ram", "Rishabh"}
print(s)