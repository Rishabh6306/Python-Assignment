# Q8. Create a list of your favorite cities, then use the index() method to find the position of a specific city on the list and print it.

cities = ["Lucknow", "Ayodhya", "Delhi", "Varanshi", "Gorkhpur", "Sultanpur"]

print(cities.index("Ayodhya"))  # Output:  1
print(cities.index("Varanshi"))  # Output: 3
print(cities.index("Sultanpur"))  # Output: 5
print(cities.index("Lucknow"))  # Output: 0