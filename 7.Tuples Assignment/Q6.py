# Q6. How do you unpack a tuple in Python? 

# For unpacking  first we right any name of all elemants in parenthesis in left side then  we define a vaible in right side. After this if we right any name and print this then we can unpack the elemants. 
t = ("Rishabh",  "Raunak", "Rahul", 20)

(green, red, black, white) = t

print(green)
print(red)
print(black)
